"""
===============================================
إضافة أطباء تجريبيين لقاعدة البيانات
===============================================
هذا الملف يضيف بعض الأطباء للتجربة
يمكن تشغيله مرة واحدة أو من داخل app.py
"""

from models.database import getdb


def seed_doctors():
    """
    إضافة أطباء تجريبيين
    
    الوظيفة:
    ---------
    - يضيف قائمة أطباء في جدول doctors
    - INSERT OR IGNORE: لا يضيف إذا الطبيب موجود
    """
    
    # =======================================
    # فتح اتصال بقاعدة البيانات
    # =======================================
    conn = getdb()
    cur = conn.cursor()

    # =======================================
    # قائمة الأطباء التجريبيين
    # =======================================
    doctors = [
        ("د. أحمد محمود", "أمراض باطنية"),
        ("د. فاطمة حسن", "طب عام"),
        ("د. محمد سالم", "أمراض الدم"),
        ("د. ليلى عبدالله", "طب الأطفال"),
        ("د. يوسف إبراهيم", "جراحة عامة"),
    ]

    # =======================================
    # إضافة الأطباء
    # =======================================
    # OR IGNORE: إذا الطبيب موجود، تجاهل (ما تضيفه مرة ثانية)
    for doctor_name, specialization in doctors:
        cur.execute("""
            INSERT OR IGNORE INTO doctors (doctor_name, specialization, is_active)
            VALUES (?, ?, 1)
        """, (doctor_name, specialization))

    # =======================================
    # حفظ وإغلاق
    # =======================================
    conn.commit()
    conn.close()
    
    print("✅ Doctors seeded successfully.")


# =======================================
# تشغيل مباشر
# =======================================
if __name__ == "__main__":
    seed_doctors()