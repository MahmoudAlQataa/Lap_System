"""
===============================================
Routes الخاصة بقائمة التقارير
===============================================
يحتوي على:
- عرض قائمة جميع التقارير (reports_list)
"""

from flask import Blueprint, render_template
from models.database import getdb

# =======================================
# إنشاء Blueprint للتقارير
# =======================================
reports_bp = Blueprint('reports', __name__)


@reports_bp.route("/reports")
def reports_list():
    """
    عرض قائمة جميع التقارير
    
    الوظيفة:
    ---------
    - جلب جميع التحاليل من قاعدة البيانات
    - عرضها في جدول مع إمكانية البحث
    
    ملاحظة:
    --------
    ✅ تغيير: الآن نعرض التحاليل (analysis_instances) مش المرضى
    فائدة: كل تحليل صف مستقل في الجدول
    """
    
    # =======================================
    # فتح اتصال بقاعدة البيانات
    # =======================================
    conn = getdb()
    cur = conn.cursor()

    # =======================================
    # جلب جميع التحاليل مع بيانات المرضى
    # =======================================
    # ✅ تغيير كبير: استعلام جديد
    # نجمع بيانات من جدولين: analysis_instances + patients
    # باستخدام JOIN
    cur.execute("""
        SELECT 
            a.id,                    -- معرف التحليل (analysis_id)
            p.patient_name,          -- اسم المريض
            p.patient_id_number,     -- رقم الهوية
            a.analysis_type,         -- نوع التحليل
            a.created_at             -- تاريخ التحليل
        FROM analysis_instances a
        JOIN patients p ON a.patient_id = p.id
        ORDER BY a.id DESC
    """)

    # =======================================
    # تخزين النتائج
    # =======================================
    # البيانات مخزنة بطريقة list of tuples
    # مثال: [(1, 'محمد', '123456', 'CBC', '2025-02-18'), ...]
    analyses = cur.fetchall()
    
    # =======================================
    # إغلاق الاتصال
    # =======================================
    conn.close()

    # =======================================
    # عرض صفحة التقارير
    # =======================================
    # ✅ تغيير: نمرر analyses بدل patients
    return render_template("reports.html", patients=analyses)
    # ملاحظة: اسم المتغير patients محفوظ للتوافق مع HTML
    # لكن فعلياً هو analyses (قائمة التحاليل)